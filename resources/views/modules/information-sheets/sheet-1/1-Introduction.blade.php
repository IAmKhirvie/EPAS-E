<div class="content-display">
    <p class="font-lg">For hundreds of years electricity has fascinated many scientists. Around 600 BC, Greek philosophers discovered that by rubbing amber against a cloth, lightweight objects would stick to it. Just like rubbing a balloon on a cloth makes the balloon stick to other objects. It was not until around the year 1600, that any real research was done on this phenomenon. A scientist by the name of Dr. William Gilbert researched the effects of amber and magnets and wrote the theory of magnetism. In fact, Dr. Gilbert was the first to use the word electric in his theory.
Dr. William Gilbert's research and theories opened the door for more discoveries into magnetism and the development of electricity.
Electricity is produce when the electrons flow on a conductor.
