    <h3>Material Classification</h3>
    <p>Materials are classified based on their ability to conduct electricity...</p>
    
    <h4>Types of Electrical Materials</h4>
    <ul>
        <li><strong>Conductors:</strong> Materials that allow easy flow of electricity (copper, aluminum)</li>
        <li><strong>Insulators:</strong> Materials that resist electricity flow (rubber, plastic)</li>
        <li><strong>Semiconductors:</strong> Materials with conductivity between conductors and insulators (silicon, germanium)</li>
        <li><strong>Superconductors:</strong> Materials with zero electrical resistance at very low temperatures</li>
    </ul>
